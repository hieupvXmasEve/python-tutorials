# List
names = ['Alice', 'Bob', 'Charlie', 'David', 'Edward']

print(names[-1]) # Edward
print(names[-2]) # David
print(names[0:3]) # ['Alice', 'Bob', 'Charlie']
print(names[2:]) # ['Charlie', 'David', 'Edward']

